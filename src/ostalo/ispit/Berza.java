public class Berza {

	
	String naziv, grad, nazivIndeksa;
	double vrednostIndeksa;
	
	
	public Berza(String naziv, String grad, String nazivIndeksa, double vrednostIndeksa) {
		this.naziv = naziv;
		this.grad = grad;
		this.nazivIndeksa = nazivIndeksa;
		this.vrednostIndeksa = vrednostIndeksa;
	}
}
